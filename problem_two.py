"""
Problem Statement:

Prompt the user to enter a sentence.
Convert the entire sentence to uppercase.
Convert the entire sentence to lowercase.
Capitalize the first word of the sentence.

Print each of these modified sentences.
"""
# SOLUTION:

sentence = input("Enter a sentence:")
print("Sentence in upper case:", sentence.upper())
print("Sentence in lower case:", sentence.lower())
print("Sentence in Capitalization:", sentence.capitalize())